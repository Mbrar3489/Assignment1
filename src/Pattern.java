public class Pattern {

    public static void main(String[] args)
            // Program to print * pattern using while loop.
    {
        int i=0;

        while(i<5)
        {
            int j=0;
            while(j<5) {
                System.out.print("*");
                j++;
            }
            System.out.println();
            i++;

        }
    }
}
